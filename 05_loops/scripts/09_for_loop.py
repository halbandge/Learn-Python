# Example 1: Iterating over each character in a string
for i in 'python':  # Loop through each letter in the string 'python'
    print(i)

print("---")

# Example 2: range(n) generates numbers from 0 to n-1
for i in range(5):  # Loop through numbers 0 to 4
    print(i)

print("---")

# Example 3: range(start, end) generates numbers from start to end-1
for i in range(2, 7):  # Loop through numbers 2 to 6
    print(i)

print("---")

# Example 4: range(start, end, step) increments by step
for i in range(0, 10, 2):  # Loop through 0, 2, 4, 6, 8
    print(i)

print("---")

# Example 5: range in reverse with negative step
for i in range(5, 0, -1):  # Loop through 5, 4, 3, 2, 1
    print(i)